package br.senai.sp.informatica.mobileb.jdbc.exemplos.MVCcursoLista.model;

import java.time.LocalDate;

import br.senai.sp.informatica.mobileb.jdbc.exemplos.MVCcursoLista.model.generic.Registro;


public class Curso implements Registro {
	private Integer id;
	private String nome;
	private LocalDate matricula;
	private String descricao;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public LocalDate getMatricula() {
		return matricula;
	}

	public void setMatricula(LocalDate matricula) {
		this.matricula = matricula;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
}
