package br.senai.sp.informatica.mobileb.jdbc.respostas.swingLibs;

import javax.swing.JFormattedTextField;


@SuppressWarnings("serial")
public class OptionalTextField extends JFormattedTextField {
	private boolean optional = false;

	public OptionalTextField() {
		super();
	}

	public OptionalTextField(AbstractFormatter formatter) {
		super(formatter);
	}

	public boolean isOptional() {
		return optional;
	}

	public void setOptional(boolean optional) {
		this.optional = optional;
	}
}
